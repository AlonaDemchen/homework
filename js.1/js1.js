let firstNumber = parseFloat(prompt("Введите первое значение:")); //вводим первое число

let secondNumber = parseFloat(prompt("Введите второе значение:")); //вводим второе число

let partialResult1 = firstNumber * 2; //первое число умнажаем на 2
let partialResult2 = secondNumber / 2; //второе число делим на 2

let result = partialResult1 + partialResult2; //вычисление результата

alert("Результат: " + result); // выводим результат на экран